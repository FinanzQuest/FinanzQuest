-- get_position_profits — scoped to one depot, only fetches prices for
-- assets the depot actually holds (avoids full table scan)
CREATE OR REPLACE FUNCTION depots.get_position_profits(p_depot_id INT)
RETURNS TABLE (
  depot_id       INT,
  asset_id       INT,
  symbol         TEXT,
  name           TEXT,
  asset_type     TEXT,
  description    TEXT,
  current_amount NUMERIC,
  total_invested NUMERIC,
  total_sold     NUMERIC,
  current_price  NUMERIC,
  market_value   NUMERIC,
  total_profit   NUMERIC
)
LANGUAGE sql STABLE AS $$
  WITH position_summary AS (
    SELECT
      t.depot_id,
      t.asset_id,
      SUM(t.amount)                                                          AS current_amount,
      SUM(CASE WHEN t.amount > 0 THEN t.amount * t.price + t.commission ELSE 0 END) AS total_invested,
      SUM(CASE WHEN t.amount < 0 THEN ABS(t.amount * t.price) - t.commission ELSE 0 END) AS total_sold
    FROM depots.transactions t
    WHERE t.depot_id = p_depot_id
    GROUP BY t.depot_id, t.asset_id
    HAVING SUM(t.amount) > 0
  ),
  latest_prices AS (
    SELECT DISTINCT ON (asset_id)
      asset_id,
      close AS current_price
    FROM api.asset_prices
    WHERE close    IS NOT NULL
      AND asset_id IN (SELECT asset_id FROM position_summary)
    ORDER BY asset_id, tstamp DESC
  )
  SELECT
    ps.depot_id,
    ps.asset_id,
    a.symbol,
    a.name,
    a.asset_type::text,
    a.description,
    ps.current_amount,
    ps.total_invested,
    ps.total_sold,
    lp.current_price,
    ps.current_amount * lp.current_price                                        AS market_value,
    ps.total_sold + (ps.current_amount * lp.current_price) - ps.total_invested  AS total_profit
  FROM position_summary ps
  JOIN api.assets a ON ps.asset_id = a.id
  LEFT JOIN latest_prices lp ON ps.asset_id = lp.asset_id;
$$;

-- get_transactions_with_position — calls get_position_profits once,
-- joins result against filtered transactions (no repeated view evaluation)
CREATE OR REPLACE FUNCTION depots.get_transactions_with_position(p_depot_id INT)
RETURNS TABLE (
  depot_id       INT,
  user_id        UUID,
  asset_id       INT,
  amount         NUMERIC,
  price          NUMERIC,
  tstamp         TIMESTAMPTZ,
  commission     NUMERIC,
  id             INT,
  symbol         TEXT,
  description    TEXT,
  asset_type     TEXT,
  current_amount NUMERIC,
  total_invested NUMERIC,
  total_sold     NUMERIC,
  total_profit   NUMERIC,
  current_price  NUMERIC,
  market_value   NUMERIC,
  type           TEXT
)
LANGUAGE sql STABLE AS $$
  SELECT
    dt.depot_id,
    dt.user_id,
    dt.asset_id,
    dt.amount,
    dt.price,
    dt.tstamp,
    dt.commission,
    dt.id,
    pp.symbol,
    pp.description,
    pp.asset_type,
    pp.current_amount,
    pp.total_invested,
    pp.total_sold,
    pp.total_profit,
    pp.current_price,
    pp.market_value,
    dt.type::text
  FROM depots.transactions dt
  LEFT JOIN depots.get_position_profits(p_depot_id) pp
    ON pp.asset_id = dt.asset_id
  WHERE dt.depot_id = p_depot_id;
$$;

-- search_assets — fuzzy search with trigram scoring
CREATE OR REPLACE FUNCTION api.search_assets(
  search_query      text,
  asset_type_filter text    DEFAULT NULL,
  result_limit      integer DEFAULT 20
)
RETURNS TABLE (
  id             bigint,
  symbol         text,
  name           text,
  description    text,
  asset_type     text,
  last_updated   date,
  relevance_score numeric
)
LANGUAGE plpgsql STABLE AS $$
BEGIN
  RETURN QUERY
  SELECT
    a.id,
    a.symbol,
    a.name,
    a.description,
    a.asset_type::text,
    a.last_updated,
    ROUND(CAST(
      CASE WHEN LOWER(a.symbol) = LOWER(search_query) THEN 100
           WHEN LOWER(a.name)   = LOWER(search_query) THEN 95
           ELSE 0 END
      + CASE WHEN LOWER(a.symbol) LIKE LOWER(search_query) || '%' THEN 50
             WHEN LOWER(a.name)   LIKE LOWER(search_query) || '%' THEN 45
             ELSE 0 END
      + similarity(a.symbol, search_query) * 40
      + similarity(a.name,   search_query) * 35
      + COALESCE(similarity(a.description, search_query) * 15, 0)
      + CASE WHEN LOWER(a.symbol) LIKE '%' || LOWER(search_query) || '%' THEN 10
             WHEN LOWER(a.name)   LIKE '%' || LOWER(search_query) || '%' THEN 8
             ELSE 0 END
    AS numeric), 2) AS relevance_score
  FROM api.assets a
  WHERE (
    a.symbol      % search_query OR
    a.name        % search_query OR
    a.description % search_query OR
    LOWER(a.symbol)      LIKE '%' || LOWER(search_query) || '%' OR
    LOWER(a.name)        LIKE '%' || LOWER(search_query) || '%' OR
    LOWER(a.description) LIKE '%' || LOWER(search_query) || '%'
  )
  AND (asset_type_filter IS NULL OR a.asset_type::text = asset_type_filter)
  ORDER BY relevance_score DESC, a.name ASC
  LIMIT result_limit;
END;
$$;

-- =============================================================================
-- MATVIEW REFRESH JOB  (TimescaleDB background worker, runs daily at 02:00)
-- Order matters: aggregated_transactions_mat before position_values_mat
-- =============================================================================

CREATE OR REPLACE PROCEDURE depots.refresh_depot_mats(job_id int, config jsonb)
LANGUAGE plpgsql AS $$
BEGIN
  REFRESH MATERIALIZED VIEW CONCURRENTLY depots.aggregated_transactions_mat;
  REFRESH MATERIALIZED VIEW CONCURRENTLY depots.position_values_mat;
END;
$$;

SELECT add_job(
  proc              => 'depots.refresh_depot_mats',
  schedule_interval => INTERVAL '1 day',
  initial_start     => date_trunc('day', NOW()) + INTERVAL '2 hours',
  scheduled         => true,
  fixed_schedule    => true
);

