CREATE OR REPLACE FUNCTION depots.transaction_affects_cash(t TransactionType)
RETURNS boolean LANGUAGE sql IMMUTABLE AS $$
  SELECT t = 'user';
$$;

CREATE OR REPLACE FUNCTION depots.transaction_affects_commission(t TransactionType)
RETURNS boolean LANGUAGE sql IMMUTABLE AS $$
  SELECT TRUE;
$$;

CREATE OR REPLACE FUNCTION depots.sp_to_per_month(period SavingsPeriod)
RETURNS NUMERIC LANGUAGE plpgsql IMMUTABLE AS $$
BEGIN
  RETURN CASE period
    WHEN 'daily'    THEN 365.0 / 12
    WHEN 'weekly'   THEN 52.0  / 12
    WHEN 'monthly'  THEN 1
    WHEN 'annually' THEN 1.0   / 12
  END;
END;
$$;

CREATE OR REPLACE FUNCTION depots.sp_to_interval(period SavingsPeriod)
RETURNS INTERVAL LANGUAGE plpgsql IMMUTABLE AS $$
BEGIN
  RETURN CASE period
    WHEN 'daily'    THEN '1 day'
    WHEN 'weekly'   THEN '1 week'
    WHEN 'monthly'  THEN '1 month'
    WHEN 'annually' THEN '1 year'
  END;
END;
$$;

